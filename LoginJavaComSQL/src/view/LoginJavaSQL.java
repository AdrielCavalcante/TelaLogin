    private void txtsenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsenhaKeyPressed
        // TODO add your handling code here:
        usuarios uso = new usuarios();
        uso.setLogin(txtlogin.getText());
        uso.setSenha(new String(txtsenha.getPassword()));
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(this.checkLogin(txtlogin.getText(), new String(txtsenha.getPassword()))){
                usuariosdao dao = new usuariosdao();
                dao.adiciona(uso);
                JOptionPane.showMessageDialog(null,txtlogin.getText()+" foi inserido no banco de dados!");
            } else {
                JOptionPane.showMessageDialog(null,"Acesso negado! Preencha todos campos");
            }
        }
    }//GEN-LAST:event_txtsenhaKeyPressed

